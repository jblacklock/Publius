    //The Candidate class represents a presidential election candidate and encapsulates the relevant information belonging thereto 
    public class Candidate
    {
        //Name is the Candidate's Name
        public string Name { get; set; }
        //ID is the candidate's candidate ID 
        public int ID { get; set; }
        //ElectoralCollegeVotes is the number of electoral college votes which the candidate has recieved in this election
        public int ElectoralCollegeVotes { get; set; }
    }